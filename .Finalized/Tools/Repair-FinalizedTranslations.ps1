param(
    [string]$FinalizedRoot = (Resolve-Path (Join-Path $PSScriptRoot '..')).Path,
    [string[]]$ModsFilter,
    [string[]]$LocalesFilter,
    [switch]$Apply,
    [switch]$FailOnIssues
)

$ErrorActionPreference = 'Stop'

. (Join-Path $PSScriptRoot 'Generate-RequestedLocalizations.ps1') -FinalizedRoot $FinalizedRoot -LibraryMode

function Get-LanguageDataEntries {
    param(
        [Parameter(Mandatory = $true)]
        [string]$FilePath
    )

    $document = New-Object System.Xml.XmlDocument
    $document.PreserveWhitespace = $true
    $document.Load($FilePath)

    $entries = [ordered]@{}
    if (-not $document.DocumentElement) {
        return [pscustomobject]@{
            Document = $document
            Entries = $entries
        }
    }

    foreach ($node in @($document.DocumentElement.ChildNodes | Where-Object { $_.NodeType -eq [System.Xml.XmlNodeType]::Element })) {
        $entries[$node.Name] = [string]$node.InnerText
    }

    [pscustomobject]@{
        Document = $document
        Entries = $entries
    }
}

function Save-LanguageDataEntries {
    param(
        [Parameter(Mandatory = $true)]
        [string]$FilePath,

        [Parameter(Mandatory = $true)]
        [hashtable]$Entries
    )

    $lines = New-Object System.Collections.Generic.List[string]
    $lines.Add('<?xml version="1.0" encoding="utf-8"?>') | Out-Null
    $lines.Add('<LanguageData>') | Out-Null

    foreach ($key in $Entries.Keys) {
        $escapedValue = [System.Security.SecurityElement]::Escape([string]$Entries[$key])
        $lines.Add("    <$key>$escapedValue</$key>") | Out-Null
    }

    $lines.Add('</LanguageData>') | Out-Null
    Set-Content -LiteralPath $FilePath -Value $lines -Encoding utf8
}

function Test-IsOneCharacterCorruption {
    param(
        [Parameter(Mandatory = $true)]
        [string]$LocalizedValue,

        [Parameter(Mandatory = $true)]
        [string]$EnglishValue
    )

    if ([string]::IsNullOrWhiteSpace($LocalizedValue) -or [string]::IsNullOrWhiteSpace($EnglishValue)) {
        return $false
    }

    $trimmedLocalized = $LocalizedValue.Trim()
    $trimmedEnglish = $EnglishValue.Trim()
    if ($trimmedEnglish.Length -le 1) {
        return $false
    }

    return $trimmedLocalized.Length -eq 1
}

function Get-EnglishFileSet {
    param(
        [Parameter(Mandatory = $true)]
        [string]$EnglishRoot
    )

    $fileSet = @{}
    if (-not (Test-Path -LiteralPath $EnglishRoot)) {
        return $fileSet
    }

    Get-ChildItem -Path $EnglishRoot -Recurse -File | ForEach-Object {
        $relativePath = Get-RelativePath -BasePath $EnglishRoot -FullPath $_.FullName
        $fileSet[$relativePath] = $_.FullName
    }

    return $fileSet
}

function Get-EnglishEntryIndex {
    param(
        [Parameter(Mandatory = $true)]
        [hashtable]$EnglishFileSet
    )

    $entryIndex = @{}
    foreach ($relativePath in $EnglishFileSet.Keys | Sort-Object) {
        if ($relativePath -notlike '*.xml') {
            continue
        }

        $englishData = Get-LanguageDataEntries -FilePath $EnglishFileSet[$relativePath]
        foreach ($key in $englishData.Entries.Keys) {
            if (-not $entryIndex.ContainsKey($key)) {
                $entryIndex[$key] = New-Object System.Collections.Generic.List[string]
            }

            $entryIndex[$key].Add($relativePath) | Out-Null
        }
    }

    return $entryIndex
}

function Repair-OrphanDuplicateFile {
    param(
        [Parameter(Mandatory = $true)]
        [string]$DuplicateFile,

        [Parameter(Mandatory = $true)]
        [string]$AutoGeneratedFile,

        [Parameter(Mandatory = $true)]
        [string]$EnglishAutoGeneratedFile,

        [Parameter(Mandatory = $true)]
        [string]$LocaleRoot,

        [Parameter(Mandatory = $true)]
        [hashtable]$EnglishFileSet,

        [Parameter(Mandatory = $true)]
        [hashtable]$EnglishEntryIndex,

        [Parameter(Mandatory = $true)]
        [switch]$ApplyChanges
    )

    $duplicateData = Get-LanguageDataEntries -FilePath $DuplicateFile
    $autoData = Get-LanguageDataEntries -FilePath $AutoGeneratedFile
    $englishData = Get-LanguageDataEntries -FilePath $EnglishAutoGeneratedFile

    $overlapKeys = New-Object System.Collections.Generic.List[string]
    $migratedKeys = New-Object System.Collections.Generic.List[string]
    $unresolvedKeys = New-Object System.Collections.Generic.List[string]
    $pendingTargetEntries = @{}

    foreach ($key in $duplicateData.Entries.Keys) {
        if ($autoData.Entries.Contains($key)) {
            $overlapKeys.Add($key) | Out-Null
            continue
        }

        if ($englishData.Entries.Contains($key)) {
            $autoData.Entries[$key] = $duplicateData.Entries[$key]
            $migratedKeys.Add($key) | Out-Null
            continue
        }

        if ($EnglishEntryIndex.ContainsKey($key)) {
            foreach ($targetRelativePath in $EnglishEntryIndex[$key]) {
                $targetLocaleFile = Join-Path $LocaleRoot $targetRelativePath
                if (-not $pendingTargetEntries.ContainsKey($targetLocaleFile)) {
                    if (Test-Path -LiteralPath $targetLocaleFile) {
                        $pendingTargetEntries[$targetLocaleFile] = (Get-LanguageDataEntries -FilePath $targetLocaleFile).Entries
                    }
                    else {
                        $pendingTargetEntries[$targetLocaleFile] = (Get-LanguageDataEntries -FilePath $EnglishFileSet[$targetRelativePath]).Entries
                    }
                }

                $pendingTargetEntries[$targetLocaleFile][$key] = $duplicateData.Entries[$key]
            }

            $migratedKeys.Add($key) | Out-Null
            continue
        }

        continue
    }

    $deleted = $false
    $updatedAuto = $false
    if ($ApplyChanges -and $unresolvedKeys.Count -eq 0 -and ($overlapKeys.Count -gt 0 -or $migratedKeys.Count -gt 0)) {
        if ($migratedKeys.Count -gt 0) {
            Save-LanguageDataEntries -FilePath $AutoGeneratedFile -Entries $autoData.Entries
            $updatedAuto = $true
        }

        foreach ($targetFile in $pendingTargetEntries.Keys) {
            Save-LanguageDataEntries -FilePath $targetFile -Entries $pendingTargetEntries[$targetFile]
        }

        Remove-Item -LiteralPath $DuplicateFile -Force
        $deleted = $true
    }

    [pscustomobject]@{
        DuplicateFile = $DuplicateFile
        AutoGeneratedFile = $AutoGeneratedFile
        OverlapCount = $overlapKeys.Count
        MigratedCount = $migratedKeys.Count
        UnresolvedCount = $unresolvedKeys.Count
        UnresolvedKeys = @($unresolvedKeys)
        UpdatedAuto = $updatedAuto
        DeletedDuplicate = $deleted
    }
}

function Repair-CorruptedEntries {
    param(
        [Parameter(Mandatory = $true)]
        [string]$EnglishFile,

        [Parameter(Mandatory = $true)]
        [string]$LocaleFile,

        [Parameter(Mandatory = $true)]
        [string]$Locale,

        [Parameter(Mandatory = $true)]
        [switch]$ApplyChanges
    )

    $englishData = Get-LanguageDataEntries -FilePath $EnglishFile
    $localeData = Get-LanguageDataEntries -FilePath $LocaleFile
    $keysToRepair = New-Object System.Collections.Generic.List[string]
    $sourceTexts = New-Object System.Collections.Generic.List[string]

    foreach ($key in $englishData.Entries.Keys) {
        if (-not $localeData.Entries.Contains($key)) {
            continue
        }

        $englishValue = [string]$englishData.Entries[$key]
        $localizedValue = [string]$localeData.Entries[$key]
        if (-not (Test-IsOneCharacterCorruption -LocalizedValue $localizedValue -EnglishValue $englishValue)) {
            continue
        }

        $keysToRepair.Add($key) | Out-Null
        $sourceTexts.Add($englishValue) | Out-Null
    }

    if (-not $keysToRepair.Count) {
        return [pscustomobject]@{
            LocaleFile = $LocaleFile
            RepairedCount = 0
            RepairedKeys = @()
            UpdatedFile = $false
        }
    }

    if ($ApplyChanges) {
        for ($index = 0; $index -lt $keysToRepair.Count; $index += 1) {
            $localeData.Entries[$keysToRepair[$index]] = [string]$sourceTexts[$index]
        }

        Save-LanguageDataEntries -FilePath $LocaleFile -Entries $localeData.Entries
    }

    [pscustomobject]@{
        LocaleFile = $LocaleFile
        RepairedCount = $keysToRepair.Count
        RepairedKeys = @($keysToRepair)
        UpdatedFile = [bool]$ApplyChanges
    }
}

$selectedLocales = if ($LocalesFilter) {
    @($locales | Where-Object { $_ -in $LocalesFilter })
}
else {
    $locales
}

$mods = Get-ChildItem -Path $FinalizedRoot -Directory | Where-Object { $_.Name -ne 'Tools' }
if ($ModsFilter) {
    $mods = @($mods | Where-Object { $_.Name -in $ModsFilter })
}

$duplicateIssues = New-Object System.Collections.Generic.List[object]
$corruptionIssues = New-Object System.Collections.Generic.List[object]

foreach ($mod in $mods) {
    $englishRoot = Join-Path $mod.FullName 'Languages\English'
    if (-not (Test-Path -LiteralPath $englishRoot)) {
        continue
    }

    $englishFileSet = Get-EnglishFileSet -EnglishRoot $englishRoot
    $englishEntryIndex = Get-EnglishEntryIndex -EnglishFileSet $englishFileSet
    $englishXmlFiles = @($englishFileSet.Keys | Where-Object { $_ -like '*.xml' })

    foreach ($locale in $selectedLocales) {
        $localeRoot = Join-Path $mod.FullName (Join-Path 'Languages' $locale)
        if (-not (Test-Path -LiteralPath $localeRoot)) {
            continue
        }

        $localeXmlFiles = @(Get-ChildItem -Path $localeRoot -Recurse -File -Filter *.xml)
        foreach ($localeFile in $localeXmlFiles) {
            $relativePath = Get-RelativePath -BasePath $localeRoot -FullPath $localeFile.FullName
            if ($englishFileSet.ContainsKey($relativePath)) {
                continue
            }

            if ($localeFile.Name -eq 'AutoGenerated.xml' -or $localeFile.BaseName -like 'Patch_*') {
                continue
            }

            $autoGeneratedFile = Join-Path $localeFile.Directory.FullName 'AutoGenerated.xml'
            if (-not (Test-Path -LiteralPath $autoGeneratedFile)) {
                continue
            }

            $englishRelativeAuto = Join-Path (Split-Path -Parent $relativePath) 'AutoGenerated.xml'
            if (-not $englishFileSet.ContainsKey($englishRelativeAuto)) {
                continue
            }

            $result = Repair-OrphanDuplicateFile -DuplicateFile $localeFile.FullName -AutoGeneratedFile $autoGeneratedFile -EnglishAutoGeneratedFile $englishFileSet[$englishRelativeAuto] -LocaleRoot $localeRoot -EnglishFileSet $englishFileSet -EnglishEntryIndex $englishEntryIndex -ApplyChanges:$Apply
            if ($result.OverlapCount -gt 0 -or $result.MigratedCount -gt 0 -or $result.UnresolvedCount -gt 0) {
                $duplicateIssues.Add([pscustomobject]@{
                    Mod = $mod.Name
                    Locale = $locale
                    Result = $result
                }) | Out-Null
            }
        }

        foreach ($relativePath in $englishXmlFiles) {
            $englishFile = $englishFileSet[$relativePath]
            $localeFile = Join-Path $localeRoot $relativePath
            if (-not (Test-Path -LiteralPath $localeFile)) {
                continue
            }

            $result = Repair-CorruptedEntries -EnglishFile $englishFile -LocaleFile $localeFile -Locale $locale -ApplyChanges:$Apply
            if ($result.RepairedCount -gt 0) {
                $corruptionIssues.Add([pscustomobject]@{
                    Mod = $mod.Name
                    Locale = $locale
                    Result = $result
                }) | Out-Null
            }
        }
    }
}

$duplicateSummary = $duplicateIssues | ForEach-Object {
    [pscustomobject]@{
        Mod = $_.Mod
        Locale = $_.Locale
        DuplicateFile = $_.Result.DuplicateFile
        Overlap = $_.Result.OverlapCount
        Migrated = $_.Result.MigratedCount
        Unresolved = $_.Result.UnresolvedCount
        Deleted = $_.Result.DeletedDuplicate
    }
}

$corruptionSummary = $corruptionIssues | ForEach-Object {
    [pscustomobject]@{
        Mod = $_.Mod
        Locale = $_.Locale
        LocaleFile = $_.Result.LocaleFile
        Repaired = $_.Result.RepairedCount
        Updated = $_.Result.UpdatedFile
    }
}

Write-Host ''
Write-Host 'Duplicate localization issues:'
if ($duplicateSummary.Count) {
    $duplicateSummary | Format-Table -AutoSize
}
else {
    Write-Host '  none'
}

Write-Host ''
Write-Host 'One-character corruption issues:'
if ($corruptionSummary.Count) {
    $corruptionSummary | Format-Table -AutoSize
}
else {
    Write-Host '  none'
}

$unresolvedDuplicates = @($duplicateIssues | Where-Object { $_.Result.UnresolvedCount -gt 0 })
$issueCount = $duplicateIssues.Count + $corruptionIssues.Count
if ($FailOnIssues -and ($issueCount -gt 0 -or $unresolvedDuplicates.Count -gt 0)) {
    throw "Detected $issueCount translation integrity issues ($($unresolvedDuplicates.Count) unresolved duplicate files)."
}